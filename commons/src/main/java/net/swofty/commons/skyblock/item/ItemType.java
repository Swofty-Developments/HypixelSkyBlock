	public final Material material;
	public final Rarity rarity;

	ItemType(Material material, Rarity rarity) {
		this.material = material;
		this.rarity = rarity;
	}

	public static @Nullable ItemType get(String name) {
		try {
			return ItemType.valueOf(name.replace("minecraft:", "").toUpperCase());
		} catch (Exception e) {
			return null;
		}
	}

	public static boolean isVanillaReplaced(String item) {
		return get(item) != null;
	}

	public static @Nullable ItemType fromMaterial(Material material) {
		String materialName = material.key().value();
		String formattedItemName = StringUtility.toNormalCase(materialName);

		for (ItemType itemType : ItemType.values()) {
			if (itemType.material == material &&
					formattedItemName.equals(StringUtility.toNormalCase(itemType.getDisplayName()))) {
				return itemType;
			}
		}
		return null;
	}

	@SneakyThrows
	public String getDisplayName() {
		return StringUtility.toNormalCase(this.name());
	}
